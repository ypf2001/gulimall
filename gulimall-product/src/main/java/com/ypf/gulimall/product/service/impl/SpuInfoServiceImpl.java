    @Override
    public PageUtils queryPageByCondition(Map<String, Object> params) {

        QueryWrapper<SpuInfoEntity> wrapper = new QueryWrapper<>();

        String key = (String) params.get("key");
        if (!StringUtils.isEmpty(key)) {
            wrapper.and((w) -> {
                w.eq("id", key).or().like("spu_name", key);
            });
        }
        // status=1 and (id=1 or spu_name like xxx)
        String status = (String) params.get("status");
        if (!StringUtils.isEmpty(status)) {
            wrapper.eq("publish_status", status);
        }

        String brandId = (String) params.get("brandId");
        if (!StringUtils.isEmpty(brandId) && !"0".equalsIgnoreCase(brandId)) {
            wrapper.eq("brand_id", brandId);
        }

        String catelogId = (String) params.get("catelogId");
        if (!StringUtils.isEmpty(catelogId) && !"0".equalsIgnoreCase(catelogId)) {
            wrapper.eq("catelog_id", catelogId);
        }

        /**
         * status: 2
         * key:
         * brandId: 9
         * catelogId: 225
         */

        IPage<SpuInfoEntity> page = this.page(new Query<SpuInfoEntity>().getPage(params), wrapper);

        return new PageUtils(page);
    }

    @Override
    /**
     * @Param: [spuId]
     * @return: void
     * @Date: 2022/6/10/23:44
     */
    public void up(Long spuId) {

        SkuEsModel esModel = new SkuEsModel();
        //查询所有规格
        List<ProductAttrValueEntity> baseAttrs = attrValueService.baseAttrlistforspu(spuId);
        //查询匹配id返回
        List<Long> attrIds = baseAttrs.stream().map(attr ->
                attr.getAttrId()
        ).collect(Collectors.toList());
        List<Long> searchAttrIds = attrService.selectSearchAttrs(attrIds);
        Set<Long> idSet = new HashSet<>(searchAttrIds);
        List<SkuEsModel.Attrs> attrValueEntities = baseAttrs.stream().filter(item ->
                idSet.contains(item.getAttrId())
        ).map(attr -> {
            SkuEsModel.Attrs attrs = new SkuEsModel.Attrs();
            BeanUtils.copyProperties(attr, attrs);
            return attrs;
        }).collect(Collectors.toList());
        //封装sku信息
        List<SkuInfoEntity> list = skuInfoService.getSkusBySpuId(spuId);
        List<Long> skuIds = list.stream().map(SkuInfoEntity::getSkuId).collect(Collectors.toList());
        Map<Long, Boolean> stockMap = null;
        try {
            R<List<SkuHasStockVo>> hasStock = wareFeignService.getSkuHasStock(skuIds);
            stockMap = hasStock.getData().stream().collect(Collectors.toMap(SkuHasStockVo::getSkuId, item -> item.getHasStock()));
        } catch (Exception e) {
            log.error("库存服务查询异常:原因{}", e);
        }
        Map<Long, Boolean> finalStockMap = stockMap;
        List<SkuEsModel> skuEsModels = list.stream().map(sku -> {
            SkuEsModel esModel1 = new SkuEsModel();
            BeanUtils.copyProperties(sku, esModel1);
            esModel1.setSkuPrice(sku.getPrice());
            esModel1.setSkuImg(sku.getSkuDefaultImg());
            //TODO 查询是否有库存
            if (finalStockMap == null) {
                esModel1.setHasStock(true);
            } else {
                esModel1.setHasStock(finalStockMap.get(sku.getSkuId()));
            }
            //TODO  热度评分
            //设置0方便测试
            esModel1.setHotScore(0L);
            //查询 品牌
            BrandEntity brand = brandService.getById(esModel1.getBrandId());
            esModel1.setBrandName(brand.getName());
            esModel1.setBrandImg(brand.getLogo());
            esModel1.setCatelogName(categoryService.getById(esModel1.getCatelogId()).getName());
            esModel1.setAttrs(attrValueEntities);

            return esModel1;
        }).collect(Collectors.toList());
        //发送到es保存
        R r = searchFeignService.productStatusUp(skuEsModels);
        if (r.getCode() == 0) {
            //修改spu状态
            baseMapper.updateSpuStatus(spuId, ProductConstant.StatusEnum.SPU_UP.getCode());
        } else {
            //调用失败 重复调用 接口幂等性

        }

    }

